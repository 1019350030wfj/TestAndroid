public class AppDevelopFlow {

    //1、产品规划，确定产品方向（要做什么）
    //2、需求调研，产出需求文档
    //3、需求评审，修改需求文档
    //4、产品狗画app线框图（产品原型图），交给设计师

    //5、设计师根据线框图，产出设计视觉稿

    //6、程序员根据设计稿，搭建Ui框架

    //7、程序员根据需求文档，开发相应功能

    //8、测试媛根据需求文档，编写相应的测试用例，安排测试时间

    //9、程序员修复测试媛提出的bug，提交beta版

    //10、bug修复完成，提交运营瞄发布到渠道上线
}
